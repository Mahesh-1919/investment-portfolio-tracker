export function buildSeedPortfolio(): StockHolding[] {
  const totalInvestment = rawHoldings.reduce(
    (sum, h) => sum + h.purchasePrice * h.qty,
    0
  );

  return rawHoldings.map((h) => {
    const investment = h.purchasePrice * h.qty;
    return {
      ...h,
      investment,
      portfolioPct: investment / totalInvestment,
      cmp: null,
      presentValue: null,
      gainLoss: null,
      gainLossPct: null,
    };
  });
}

export const TOTAL_SEED_INVESTMENT = rawHoldings.reduce(
  (sum, h) => sum + h.purchasePrice * h.qty,
  0
);
